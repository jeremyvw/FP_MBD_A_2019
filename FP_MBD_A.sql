-->> Menampilkan data nama cabang, nama roti, tanggal jual, jumlah roti terjual pada bulan tertentu
CREATE OR REPLACE VIEW ROTI_TERJUAL
AS
  SELECT NVL(CAB.NAMACABANG,'-')NAMACABANG,NAMAROTI,NVL(TO_CHAR(TGLJUAL,'MM-YYYY'),'-') PERIODE, NVL(SUM(JUMJUAL),0) TOTAL_JUAL
  FROM ROTI 
  left join DJUAL
  on ROTI.KODEROTI    = DJUAL.KODEROTI
  left join HJUAL
  on HJUAL.NONOTAJUAL = DJUAL.NONOTAJUAL
  left join CABANG CAB
  on CAB.KODECABANG   = HJUAL.KODECABANG
  GROUP BY CAB.NAMACABANG,NAMAROTI,TO_CHAR(TGLJUAL,'MM-YYYY');

SELECT * FROM ROTI_TERJUAL
ORDER BY NAMACABANG ASC, PERIODE DESC, TOTAL_JUAL DESC;
 


   koderoti  = :new.koderoti;
  update hjual set totaljual = totaljual + (:new.hargajual * :new.jumjual)
  where nonotajual = :new.nonotajual;
   elsif updating then
     update stokcabang 
     set jumterjual = jumterjual+:new.jumjual-:old.jumjual
  where kodecabang = (select kodecabang from hjual
                      where nonotajual=:new.nonotajual) and
      koderoti  = :new.koderoti;
  update hjual set totaljual = totaljual + (:new.hargajual * :new.jumjual)
                               - (:old.hargajual * :old.jumjual)
  where nonotajual = :new.nonotajual;
   elsif deleting then
     update stokcabang 
     set jumterjual = jumterjual-:old.jumjual
  where kodecabang = (select kodecabang from hjual

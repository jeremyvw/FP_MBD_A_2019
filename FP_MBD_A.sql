CREATE OR REPLACE TRIGGER TAMBAH_STOKROTI
AFTER INSERT ON DPRODUKSI
FOR EACH ROW
DECLARE
  JUMCAB INT;
  TEMPJUM INT;
  JUMSETIAPCABANG INT;
  TGLHEADER DATE; 
  PERNAH INT;
BEGIN
  UPDATE ROTI SET STOKROTI = STOKROTI + :NEW.JUMPROD
  WHERE KODEROTI = :NEW.KODEROTI;
  SELECT COUNT(*) INTO JUMCAB FROM CABANG;
  JUMSETIAPCABANG := FLOOR(:NEW.JUMPROD/JUMCAB);
  TEMPJUM := :NEW.JUMPROD;
  SELECT TGLPROD INTO TGLHEADER FROM HPRODUKSI
  WHERE NONOTAPROD = :NEW.NONOTAPROD;
  FOR I IN (SELECT * FROM CABANG) LOOP
     SELECT COUNT(*) INTO PERNAH FROM STOKCABANG
     WHERE KODECABANG = I.KODECABANG 
        AND KODEROTI = :NEW.KODEROTI AND
        TO_CHAR(TGLSTOKCAB,'DD/MM/YYYY') = TO_CHAR(TGLHEADER,'DD/MM/YYYY');
     IF (PERNAH>0) THEN
       UPDATE STOKCABANG 
       SET JUMTERIMA = JUMTERIMA + JUMSETIAPCABANG
       WHERE KODECABANG = I.KODECABANG 
        AND KODEROTI = :NEW.KODEROTI AND
        TO_CHAR(TGLSTOKCAB,'DD/MM/YYYY') = TO_CHAR(TGLHEADER,'DD/MM/YYYY');
     ELSE
       INSERT INTO STOKCABANG VALUES(I.KODECABANG, TO_DATE(TO_CHAR(SYSDATE,'DD/MM/YYYY'),'DD-MM-YYYY'),:NEW.KODEROTI,JUMSETIAPCABANG,0);
     END IF;
     TEMPJUM := TEMPJUM-JUMSETIAPCABANG;
     IF TEMPJUM-2*JUMSETIAPCABANG<0 THEN
         JUMSETIAPCABANG :=TEMPJUM;
     END IF;
  END LOOP;
END;
/
SHOW ERR;

--> CEK BAHAN PRODUKSI
CREATE OR REPLACE FUNCTION CUKUPKAH_BAHAN_PRODUKSI
(kode varchar2, jum varchar2)
return varchar2 is 
   cukup VARCHAR2(5) := 'YA';
begin
   for i in (select stokbhn,jumperlu from bahan,RESEP
             where koderoti = kode and
    bahan.kodebhn = resep.kodebhn
    ) loop
      if jum*i.jumperlu>i.stokbhn then
      cukup:='TIDAK';
   end if;
   end loop;
   return cukup;
end;
/
show err;

-->CEK PRODUKSI
create or replace trigger cek_produksi
before insert on dproduksi 
for each row
begin
  if (CUKUPKAH_BAHAN_PRODUKSI(:new.koderoti,:new.jumprod)='TIDAK') THEN
     raise_application_error(-20001,'stok tidak mencukup');
  end if;
end;
/
show err;

-->CEK STOKCABANG
create or replace trigger cek_jual_cabang
before insert or update on stokcabang
for each row
begin
   if :old.jumterima < :new.jumterjual then
      raise_application_error(-20002,'stok pada cabang tidak mencukupi');
   end if;
end;
/
show err;

